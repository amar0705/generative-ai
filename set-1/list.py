# Create a list of numbers from 1 to 10
numbers = list(range(1, 11))
print("Initial list:", numbers)

# Add a number
numbers.append(20)
print("List after adding 20:", numbers)

# Remove a number
numbers.remove(3)
print("List after removing 3:", numbers)

# Sort the list
numbers.sort()
print("List after sorting:", numbers)
